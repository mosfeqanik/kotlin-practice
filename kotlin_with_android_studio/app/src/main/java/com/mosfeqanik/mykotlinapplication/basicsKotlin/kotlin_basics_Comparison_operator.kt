package com.mosfeqanik.myfirstapp

fun main() {

    //Comparison operator(==,!=,<,>,<=,>=)

    var isEqual = 3==3
    //String Interpolation
    println("3 isEqual 3 $isEqual")
    var isnotEqual = 5!=3
    println("5 isnotEqual 3 $isnotEqual")
    println("5 isGreater than 3 ${5>3}")
    println("-5 isGreater than 3 ${-5>3}")
    println("5 isLess than 3 ${5<3}")
    println("3 isLess than or equal 5 ${3<=5}")

}
