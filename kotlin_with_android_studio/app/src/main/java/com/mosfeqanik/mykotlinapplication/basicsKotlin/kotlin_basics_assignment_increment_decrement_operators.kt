package com.mosfeqanik.myfirstapp

fun main() {

    ///assignment increment decrement operators
    var myNum = 5
    println("myNum = 5 $myNum")
    myNum += 3
    //8
    println("myNum += 3 $myNum")
    myNum += 2
    println("myNum += 2 $myNum")
    myNum *= 2
    println("myNum *= 2 $myNum")

    //increment decrement
    myNum++
    println("myNum++ $myNum")

    println("myNum++ $myNum")

    println("myNum++ ${myNum++}")
    println("myNum $myNum")
    println("myNum++ ${++myNum}")


}
